手機1={'廠牌':'蘋果','型號':'iPhone 12 Pro 5G','容量':'256 GB','顏色':'銀色'}
手機2={'廠牌':'三星','型號':'Galaxy S22 Ultra','容量':'256 GB','顏色':'夜幕紅'}
庫存=[手機1,手機2]
查詢=input("請輸入要查詢的手機廠牌名稱:")
